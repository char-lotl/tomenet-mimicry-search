const toCamelCase = function(str) {
  return str.replace(/-([a-z])/g, (match, letter) => {
    return letter.toUpperCase();
  });
}

// A cycler that finds the next element of an array with unique primitive entries,
// cycling around to the start if it reaches the end.
const cycleArray = (function() {
  indices = {};
  lengths = {};
  return function(array, value) {
    if (!Object.hasOwn(indices, array)) {
      indices[array] = 0;
      lengths[array] = array.length;
    }
    if (array[indices[array]] !== value) indices[array] = array.indexOf(value);
    indices[array] = (indices[array] + 1) % lengths[array];
    return array[indices[array]];
  }
})();

