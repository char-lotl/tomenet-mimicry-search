/* Use this instead of writing directly to button state, so that it gets mirrored
   to allInputs, which is important for CSS filtering of results. */
const writeToState = function(button, value) {
  button.dataset.state = value;
  allInputs.dataset[toCamelCase(button.id) + "State"] = button.dataset.state;
};

const cycleState = function(button) {
  if (Object.keys(nontrivialCycles).includes(button.id)) {
    writeToState(button, cycleArray(nontrivialCycles[button.id], button.dataset.state));
  } else {
    if (button.dataset.state === "inactive")
      writeToState(button, "active");
    else writeToState(button, "inactive");
  }
};

const handleLinkedButtons = function(button) {
  if (button.id === "full-equip-button")
    limbButtons.forEach(b => writeToState(b, button.dataset.state));

  if (limbButtons.includes(button)) {
    if (button.dataset.state === "active") {
      if (limbButtons.every(b => b.dataset.state === "active"))
        writeToState(fullEquipButton, "active");
    } else {
      writeToState(fullEquipButton, "inactive");
    }
  }
};

const applyRangeFilter = function(name, value) {
  const isBlank = (value === "");
  const [property, boundType] = name.split("-");
  const isLower = (boundType === "lower");
  const outOfBoundsType = isLower ? "-low" : "-high";
  for (var i = resultTiles.length - 1; i >= 0; i--) {
    const inRange = isLower ?
      monsterStats[i][indicesByName[property]] >= value :
      monsterStats[i][indicesByName[property]] <= value;
    if (inRange || isBlank) {
      resultTiles[i].removeAttribute("data-" + property + outOfBoundsType);
    } else {
      resultTiles[i].setAttribute("data-" + property + outOfBoundsType, "");
    }
  }
};

allInputs.addEventListener('click', e => {
  const button = e.target.closest('button');
  if (!button) return;

  cycleState(button);
  handleLinkedButtons(button);
});

allInputs.addEventListener('input', e => {
  const changedFieldName = e.target.name;
  const value = e.target.value;
  if (e.target.type === 'text') {
    const validInput = patternSelector[changedFieldName].test(value);
    if (validInput) {
      e.target.removeAttribute("data-invalid");
      applyRangeFilter(changedFieldName, value);
    } else {
      e.target.setAttribute("data-invalid", "");
    }
  }
});
